export interface ZoneExit {
  tileX: number;
  tileY: number;
  toZoneId: string;
  toTileX: number;
  toTileY: number;
}

export interface ZoneConfig {
  mapData: number[][];
  cols: number;
  rows: number;
  spawn: { x: number; y: number };
  exits: ZoneExit[];
}

